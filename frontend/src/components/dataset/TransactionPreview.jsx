import { useState, useMemo } from 'react'
import { motion, AnimatePresence } from 'framer-motion'
import { Search, Info, HelpCircle, Columns, ListFilter, Hash, Sparkles } from 'lucide-react'
import EmptyState from './EmptyState'

export default function TransactionPreview({ txData }) {
  const [search, setSearch] = useState('')

  if (!txData) return null
  const { transactions = [], txCols = [], stats = {} } = txData

  // Filter transactions
  const filtered = useMemo(() => {
    if (!search.trim()) return transactions
    const q = search.toLowerCase()
    return transactions.filter((tx) =>
      tx.some((item) => String(item).toLowerCase().includes(q))
    )
  }, [transactions, search])

  // Get first 15 filtered
  const previewList = filtered.slice(0, 15)

  // Color map for items to make them look premium
  const getItemColor = (item) => {
    let hash = 0
    const str = String(item)
    for (let i = 0; i < str.length; i++) {
      hash = str.charCodeAt(i) + ((hash << 5) - hash)
    }
    const colors = [
      'bg-blue-50 text-blue-700 border-blue-150',
      'bg-emerald-50 text-emerald-700 border-emerald-150',
      'bg-violet-50 text-violet-700 border-violet-150',
      'bg-amber-50 text-amber-700 border-amber-150',
      'bg-pink-50 text-pink-700 border-pink-150',
      'bg-cyan-50 text-cyan-700 border-cyan-150',
      'bg-indigo-50 text-indigo-700 border-indigo-150',
    ]
    return colors[Math.abs(hash) % colors.length]
  }

  return (
    <div className="space-y-6">
      {/* Transaction stats row */}
      <div className="grid grid-cols-2 lg:grid-cols-4 gap-3">
        {[
          { label: 'Total Transactions', value: stats.total.toLocaleString(), suffix: ' records' },
          { label: 'Avg Items / Tx', value: stats.avgItems, suffix: ' items' },
          { label: 'Largest Transaction', value: stats.maxItems, suffix: ' items' },
          { label: 'Smallest Transaction', value: stats.minItems, suffix: ' items' },
        ].map((stat, idx) => (
          <div key={idx} className="bg-slate-50 border border-slate-200 rounded-xl p-3.5 flex flex-col justify-center">
            <span className="text-[10px] text-slate-400 font-semibold uppercase tracking-wider">{stat.label}</span>
            <div className="flex items-baseline gap-1 mt-1">
              <span className="text-lg font-extrabold text-slate-800">{stat.value}</span>
              <span className="text-[10px] text-slate-400 font-medium">{stat.suffix}</span>
            </div>
          </div>
        ))}
      </div>

      {/* Info message */}
      <div className="p-3 bg-blue-50/50 border border-blue-100 rounded-xl flex items-start gap-2.5">
        <Info className="w-4 h-4 text-blue-500 shrink-0 mt-0.5" />
        <p className="text-[11px] text-slate-600 leading-relaxed">
          Healthcare transactions are generated by extracting values from non-numeric columns:{' '}
          <span className="font-semibold">{txCols.join(', ')}</span>. Empty fields and ID columns are ignored to construct clean transaction itemsets.
        </p>
      </div>

      {/* Search & Header */}
      <div className="flex flex-col sm:flex-row sm:items-center justify-between gap-3 pt-2">
        <h3 className="text-xs font-bold text-slate-400 uppercase tracking-wider">
          Transaction List Preview (First 15 Rows)
        </h3>
        
        <div className="relative w-full sm:max-w-xs">
          <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-3.5 h-3.5 text-slate-400" />
          <input
            type="text"
            value={search}
            onChange={(e) => setSearch(e.target.value)}
            placeholder="Search items in transactions…"
            className="w-full pl-9 pr-4 py-2 text-xs bg-white border border-slate-200 rounded-lg text-slate-700 placeholder-slate-400 focus:outline-none focus:ring-2 focus:ring-blue-500/20 focus:border-blue-400 transition-all"
          />
        </div>
      </div>

      {/* Transactions list */}
      <AnimatePresence mode="wait">
        {filtered.length === 0 ? (
          <EmptyState
            key="empty"
            icon={Sparkles}
            title="No transactions match"
            description="No transaction records contain items matching your search query."
          />
        ) : (
          <motion.div
            key="list"
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            exit={{ opacity: 0 }}
            className="border border-slate-250 rounded-xl overflow-hidden shadow-sm"
          >
            <div className="overflow-x-auto">
              <table className="w-full text-xs">
                <thead>
                  <tr className="bg-slate-900 text-slate-300">
                    <th className="w-12 px-4 py-3 text-center font-semibold text-slate-500 border-b border-slate-700 sticky left-0 bg-slate-900">
                      TxID
                    </th>
                    <th className="px-4 py-3 text-left font-semibold border-b border-slate-700">
                      Transaction Itemset
                    </th>
                    <th className="w-20 px-4 py-3 text-center font-semibold text-slate-500 border-b border-slate-700">
                      Size
                    </th>
                  </tr>
                </thead>
                <tbody>
                  {previewList.map((tx, idx) => (
                    <tr
                      key={idx}
                      className="border-b border-slate-100 last:border-0 hover:bg-slate-50/50 transition-colors"
                    >
                      <td className="px-4 py-3 text-center font-mono text-[10px] text-slate-400 bg-slate-50/30 border-r border-slate-100 sticky left-0">
                        {String(idx + 1).padStart(3, '0')}
                      </td>
                      <td className="px-4 py-3">
                        <div className="flex flex-wrap gap-1.5 max-w-2xl">
                          {tx.map((item, iidx) => (
                            <span
                              key={iidx}
                              className={`px-2 py-0.5 rounded text-[10px] font-semibold border ${getItemColor(item)}`}
                            >
                              {item}
                            </span>
                          ))}
                        </div>
                      </td>
                      <td className="px-4 py-3 text-center font-bold text-slate-750 tabular-nums">
                        {tx.length}
                      </td>
                    </tr>
                  ))}
                </tbody>
              </table>
            </div>
          </motion.div>
        )}
      </AnimatePresence>
    </div>
  )
}
